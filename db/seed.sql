CREATE TABLE party (
  "partyid" SERIAL PRIMARY KEY,
  "partyname" VARCHAR(30),
  "scenarioid" integer references scenarios(scenarioid),
  "achievementsid" integer references achievements(achievementsid),
  "citydeckid" integer references citydeck(citydeckid),
  "roaddeckid" integer references roaddeck(roaddeckid)
);


SELECT * FROM scenarios JOIN party ON scenarios.scenarioid = party.scenarioid;
SELECT * FROM achievements JOIN party ON achievements.achievementsid = party.achievementsid;
SELECT * FROM citydeck JOIN party ON citydeck.citydeckid = party.citydeckid;
SELECT * FROM roaddeck JOIN party ON roaddeck.roaddeckid = party.roaddeckid;

CREATE TABLE usersparties (
userid integer references users(userid),
partyid integer references party(partyid),
characterchoice text,
charactername text
);
SELECT * FROM users JOIN usersparties ON users.userid = usersparties.userid;
SELECT * FROM party JOIN usersparties ON party.partyid = usersparties.partyid;

